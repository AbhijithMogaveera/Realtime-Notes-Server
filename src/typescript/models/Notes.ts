class Note{

    title:String
    content:String
    owner_id:String
    creation_date:String
    color:String


    constructor(
        title:String,
        content:String,
        owner_id:String,
        creation_date:String,
        color:String
    ){
        this.title = title
        this.content = content
        this.owner_id = owner_id
        this.creation_date = creation_date
        this.color = color
    }
}